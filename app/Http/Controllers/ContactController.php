    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'email'=>'required',
            'subyek'=>'required',
            'message'=>'required'
        ]);

        $userID = Auth::user()->id;

        Contact::Insert([
            'user_id' => $userID,
            'email' => $request->get('email'),
            'subyek' => $request->get('subyek'),
            'message' => $request->get('message'),
            'updated_at' => now()
        ]);

        return redirect('contact')->with(['success' => 'Tiket telah dibuat ! Tunggu email balasan dari kami.']);
    }

